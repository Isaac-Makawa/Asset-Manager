package mw.gov.agriculture.assestmanager.models;

/**
 *
 * @author Emilda Makawa
 */
public class AssetsCount {
    String name;
    int count;

    public AssetsCount(String name,int count){
        this.name=name;
        this.count=count;
    }
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
    
}
