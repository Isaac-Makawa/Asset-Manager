    private void setInstitutions(){
        ArrayList<Institution> insts=user.getInstitutions();
        if(insts==null){
            System.out.println("Error");
        }else{
            DefaultComboBoxModel <ComboBoxItem>model=new DefaultComboBoxModel<>();
            for(Institution institution: insts){
                model.addElement(new ComboBoxItem(institution.getId(),institution.getName()));
            }
            user_institution.setModel(model);
        }
    } 
