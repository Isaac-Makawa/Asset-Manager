    private void user_save_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_user_save_btnActionPerformed
        String fname=user_fname.getText();        
        String lname=user_sname.getText();
        String email=user_email.getText();
        String password=new String(user_password_field.getPassword());
        String role=(String) user_role.getSelectedItem();
        
       //getting selected institution
       ComboBoxItem item=(ComboBoxItem) user_institution.getSelectedItem();
       Institution institution=new Institution();
       institution.setId(item.getId());
       institution.setName(item.getName());
       
       User user=new User();// creating user object and assigneing details from the form
       user.setFname(fname);
       user.setLname(lname);
       user.setEmail(email);
       String hash=BCrypt.withDefaults().hashToString(12,password.toCharArray());
       user.setPassword(hash);
       user.setRole(role);
       user.setInstitution(institution);
       
       boolean result=this.user.addUser(user);
       if(result){
           JOptionPane.showMessageDialog(this,"User has been added successfully","Success", JOptionPane.INFORMATION_MESSAGE);
       }else{
           JOptionPane.showMessageDialog(this,"Something went wrong","Error", JOptionPane.ERROR_MESSAGE);
       }
       user_sname.setText("");
       user_password_field.setText("");
       user_email.setText("");
       user_fname.setText("");
    }//GEN-LAST:event_user_save_btnActionPerformed

